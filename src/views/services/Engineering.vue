import API from '../../api';
import { mapGetters } from 'vuex';
import Header from '../../components/Header.vue';
import Footer from '../../components/Footer.vue';

export default {
    name: 'Engineering',
    components: {Header, Footer},
    data() {
        return {
            lang: '',
            img: []
        }
    },
    methods: {
        scrollGallery(dir) {
            var div = document.getElementById("gallery-photos");
            switch(dir) {
                case 'left':
                    div.scrollBy({
                        top: 0,
                        left: -250,
                        behavior: "smooth"
                    });
                    break;
                case 'right':
                    div.scrollBy({
                        top: 0,
                        left: 250,
                        behavior: "smooth"
                    });
                    break;
            }
        },
        getServiceGallery() {
            var config = {};
            if(this.lang == 'ru') {
                config = {
                    headers: {'Accept-Language': 'ru'}
                };    
            }
            if(this.lang == 'uz') {
                config = {
                    headers: {'Accept-Language': 'uz'}
                };
            }
            API.get('/api/gallery', config)
                .then(res => {
                    console.log(res.data);
                    res.data.datas.forEach(element => {
                        var uri = 'https://back.alfabestservis.uz/storage/' + element.image;
                        this.img.push(uri);
                    });
                })
        }
    },
    computed: {
        ...mapGetters(['getSelectedLang']),
        getLang() {
            var lang = this.getSelectedLang;
            this.lang = lang;
        }
    },
    mounted() {
        this.getLang;
        this.getServiceGallery();
    }
}
</script>

